// Objects in JavaScript group related data and functions together.

// Basic Example:

var myProperty = {
  address: "32 Main",
  city: "Toronto",
  purchaseYear: 2016
};

myProperty

// Console results:
// Object {address:"32 Main", city: "Toronto", purchaseYear: 2016}

// Accesing an specific part(data) of the Object:
myProperty.city
//  Console results:
// "Toronto"

myProperty.address
//  Console results:
// "32 Main"

myProperty.purchaseYear
//  Console results:
// "2016"
